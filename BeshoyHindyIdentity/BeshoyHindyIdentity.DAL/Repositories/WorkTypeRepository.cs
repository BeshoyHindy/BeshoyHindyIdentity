        public DataTable LoadByActiveState(WorkType WorkTyperam, String SortField, String SortType)
        {
            try
            {
                if (WorkTyperam.Id != null)
                {
                    var Query = (from usr in db.WorkTypes
                                 where usr.Active == WorkTyperam.Active && usr.Id == WorkTyperam.Id
                                 select usr);
                    return Query.ToDataTable(SortField, SortType);

                }
                else if (WorkTyperam.Id == null)
                {
                    return null;
                }
                return null;

            }
            catch (Exception Ex)
            {
                return null;
            }

        }
        public DataTable LoadByActiveState(Boolean ActiveStatePram, String SortField = "RecOrder", String SortType = "ASC")
        {
            var Query = (from usr in db.WorkTypes
                         where usr.Active == ActiveStatePram
                         select usr);
            return Query.ToDataTable(SortField, SortType);
        }
        public DataTable LoadWorksTypes()
        {
            var Query = (from wrktype in db.WorkTypes
                         where wrktype.Active == true
                         select wrktype);
            return Query.ToDataTable("RecOrder", "ASC");
        }

        public WorkType LoadByWorkType(WorkType WorkTyperam)
        {
