    public class WorkTypeRepository
    {
        #region Variables
        DBDataContext db;
        public WorkType _Obj { get; set; }
        #endregion
        #region Methods
        public WorkTypeRepository()
        {
            db = new DBDataContext();
            _Obj = new WorkType();
        }
        public Guid Add()
        {
            _Obj.Id = Guid.NewGuid(); //get next or new id
            _Obj.Active = true;
            _Obj.RecOrder = LoadItemsCount(true) + 1;
            _Obj.CreatedOn = DateTime.Now;
            db.WorkTypes.Add(_Obj);
            db.SaveChanges();
            return _Obj.Id;
        }
        public Boolean Edit(WorkType WorkTyperam)
        {
            try
